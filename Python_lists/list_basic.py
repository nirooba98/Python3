fruit = ["Apple", "Pineapple", "Orange", "Strawberry"]      # A list with multiple string values
print(fruit)                    # prints the whole list
print(fruit[2])                 # prints only the value at index 2  "Orange"
print(fruit[-4])                # prints only the value at index -4 "Apple"

