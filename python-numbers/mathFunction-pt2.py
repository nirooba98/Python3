from math import *        # you need to import this line to perform certain math functions

"""
'math' is a built-in module in python. It lets you perform wide range of
mathematical functions/operations. only a few basic ones are listed below

"""
num1 = 144
num2 = 7.89
print(sqrt(num1))           # prints the square root of the number
print(floor(num2))          # prints the round up value. Lowers the value to the nearest integer
print(ceil(num2))           # prints the round up value. Rises the value to the nearest integer
print(pi)                   # returns the 'pi' value
