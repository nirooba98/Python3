"""
Examples of some basic built-in math functions in python
"""
x = 10
y = 27
z = -6.82
r = 7.48
print(min(x, y, z))     # minimum of two numbers
print(max(x, y, r))     # maximum of two numbers
print(abs(z))           # prints absolute value of the number
print(round(r))         # prints the round up value of the number

print(pow(5, 3))    # function takes two arguments. 1st one is the number and 2nd one is its exponent value.

