x = 12     # integer value
y = 5.5    # float value
z = 1+2j   # complex number
print(x, y, z)
print(complex(x))           # converts integer to complex number
print(int(y))               # converts float to integer number
print(z)                    # cannot convert complex number into integer or floating number
print(complex(3.5))         # converts float to complex number
