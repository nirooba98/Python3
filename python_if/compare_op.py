print("Find the greatest number")
a = input("Type in the first number: ")
b = input("Type in the second number: ")
if a > b:
    print(str(a) + " is the greatest number")
elif a == b:
    print("Both are equal numbers.")
else:
    print(str(b) + " is the greatest number")
